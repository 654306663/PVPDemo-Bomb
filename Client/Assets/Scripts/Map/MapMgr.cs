public class MapMgr : MonoBehaviour
{
    public static MapMgr Instance;

    MapData mapData;

    Dictionary<MapItemType, GameObject> mapItemPrefab;

    public List<Vector3> emptyItemList;

    void Awake()
    {
        Instance = this;

        mapData = new MapData();
        mapItemPrefab = new Dictionary<MapItemType, GameObject>();

        mapItemPrefab.Add(MapItemType.InnerWall, Resources.Load("Prefabs/InnerWall") as GameObject);
        mapItemPrefab.Add(MapItemType.OutWall, Resources.Load("Prefabs/OutWall") as GameObject);

        emptyItemList = new List<Vector3>();

        CreateMap();
    }

    public void CreateMap()
    {
        int row = mapData.map01.GetLength(0);
        int column = mapData.map01.GetLength(1);

        int startXPos = -column;
        int startZPos = row ;

        for (int i = 0; i < row; i++)
        {
            for (int j = 0; j < column; j++)
            {
                switch (mapData.map01[i, j])
                {
                    case 0:
                        emptyItemList.Add(new Vector3(j * 2 + startXPos, 0, startZPos - i * 2));
                        break;
                    case 1:
                        GameObject outerWall = Instantiate(mapItemPrefab[MapItemType.OutWall], new Vector3(j * 2 + startXPos, 1, startZPos - i * 2), Quaternion.identity) as GameObject;
                        outerWall.name = i + " " + j;
                        outerWall.transform.SetParent(GetParent());
                        break;
                    case 2:
                        GameObject innerWall = Instantiate(mapItemPrefab[MapItemType.InnerWall], new Vector3(j * 2 + startXPos, 1, startZPos - i * 2), Quaternion.identity) as GameObject;
                        innerWall.name = i + " " + j;
                        innerWall.transform.SetParent(GetParent());
                        break;
                }
            }
        }
    }

    Transform itemParent;
    public Transform GetParent()
    {
        if (itemParent == null)
        {
            itemParent = new GameObject("Map").transform;
            itemParent.transform.position = Vector3.zero;
        }
        return itemParent;
    }
}
